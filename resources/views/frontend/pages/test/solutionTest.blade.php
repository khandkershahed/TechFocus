<script>
    document.addEventListener('DOMContentLoaded', function() {
        const loadMoreBtn = document.getElementById('loadMoreBtn');
        const items = document.querySelectorAll('.solution-item');
        let visibleCount = 8; // initially show 8

        // Hide items >8
        items.forEach((item, index) => {
            if (index >= 8) item.classList.add('d-none');
        });

        loadMoreBtn.addEventListener('click', function() {
            const total = items.length;

            if (visibleCount >= total) {
                // Collapse back to 8 items
                items.forEach((item, index) => {
                    item.classList.toggle('d-none', index >= 8);
                });
                visibleCount = 8;
                loadMoreBtn.textContent = 'View More';
            } else {
                // Show next 4 items
                const nextCount = Math.min(visibleCount + 4, total);
                for (let i = visibleCount; i < nextCount; i++) {
                    items[i].classList.remove('d-none');
                }
                visibleCount = nextCount;

                if (visibleCount >= total) {
                    loadMoreBtn.textContent = 'Collapse All';
                }
            }
        });
    });
</script>

